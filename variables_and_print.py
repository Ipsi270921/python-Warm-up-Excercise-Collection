# Assign the string "Isu" to the variable 'name' representing a person's name
name = "Isu"

# Assign the string "Mumbai" to the variable 'city' representing a location
city = "Mumbai"

# Assign a list of hobbies (strings) to the variable 'hobbies'
hobbies = ["Dancing", "Traveling", "Music"]

# Print a formatted string that combines the above variables into a readable sentence
# Using an f-string to embed variables directly into the string
# ', '.join(hobbies) converts the list of hobbies into a single string separated by commas
print(f"My name is {name},  I live in {city} and my hobbies are {', '.join(hobbies)}.")
